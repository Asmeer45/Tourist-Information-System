package model;

public class Item {

	
	private int id;
    private String name;
    private String description;
    private String latitude;
    private String longitude;
    private String multimedia_url;
	


	    // Getter and Setter for id
	    public int getId() {
	        return id;
	    }

	    public void setId(int id) {
	        this.id = id;
	    }

	    // Getter and Setter for name
	    public String getName() {
	        return name;
	    }

	    public void setName(String name) {
	        this.name = name;
	    }

	    // Getter and Setter for description
	    public String getDescription() {
	        return description;
	    }

	    public void setDescription(String description) {
	        this.description = description;
	    }

	    // Getter and Setter for latitude
	    public String getLatitude() {
	        return latitude;
	    }

	    public void setLatitude(String latitude) {
	        this.latitude = latitude;
	    }

	    // Getter and Setter for longitude
	    public String getLongitude() {
	        return longitude;
	    }

	    public void setLongitude(String longitude) {
	        this.longitude = longitude;
	    }

	    // Getter and Setter for multimedia_url
	    public String getMultimedia_url() {
	        return multimedia_url;
	    }

	    public void setMultimedia_url(String multimedia_url) {
	        this.multimedia_url = multimedia_url;
	    }
	
	
}
